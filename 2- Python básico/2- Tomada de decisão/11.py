# If  e Else- Se a condição for verdadeira ele ira ser executado


# Operadores relacionais
# == : igual
# >  : maior
# >= : maior igual
# <  : menor
# <= : menor igual
# != : diferente 

n = int(input('Digite seu número: '))

if n > 0:
    print('Número é positivo')
else:
    print('Número é negativo!')